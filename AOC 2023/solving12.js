function entry(p_string) {
	var tokens, tokens2;
	var springs = [];
	tokens = p_string.split(" ");
	tokens2 = tokens[1].split(",");
	springs = [];
	tokens2.forEach(str2 => {
		springs.push(parseInt(str2, 10))
	});
	return {record : stringToCharArray(tokens[0]), springs : springs.slice()}
}


var gOffsetIR, gOffsetIS, gCombinations;

function findAnswer(p_entry) {
	// Move until we meet the first '?' character in the record
	const firstUnknown = continueToFirstUnknown(p_entry.record, p_entry.springs, 0, 0, false);
	if (firstUnknown.failed) {
		return 0;
	}
	gOffsetIR = firstUnknown.ir;
	gOffsetIS = firstUnknown.is;
	gCombinations = generateDoubleEntryArray(p_entry.record.length - gOffsetIR, p_entry.springs.length - gOffsetIS, -1);
	return findAnswer_aux(p_entry.record, p_entry.springs, firstUnknown.ir, firstUnknown.is);
}

// Run the known string from (p_ir, p_is) until a '?' character is met, or we have reached the end. It is possible that this method is called immediately after a '?' character was treated as a sharp.
function continueToFirstUnknown(p_record, p_springs, p_ir, p_is, p_justMetASharp) {
	var countSharps = (p_justMetASharp ? 1 : 0);
	var ir = p_ir;
	var is = p_is;
	var sharpToleranceBeginning = false; 
	while (ir < p_record.length && (p_record[ir] != '?' || sharpToleranceBeginning)) {
		// Right now, ir == 0 OR char in position ir-1 is a '.' or a '?' treated as a '.' (EXCEPTION : p_justMetASharp at the beginning)
		while (ir < p_record.length && (p_record[ir] != '?' || sharpToleranceBeginning)) {
			if (p_record[ir] == '.') {
				if (countSharps > 0) {
					if (countSharps != p_springs[is]) {
						return {failed : true};
					} 
					is++;
					if (is == p_springs.length) {
						// Check we don't have any #s left
						for ( ; ir <  p_record.length ; ir++) {
							if (p_record[ir] == '#') {
								return {failed : true};
							}
						}
					}
					countSharps = 0;
				}
			} else {
				countSharps++;
				if (countSharps > p_springs[is]) {
					return {failed : true};
				}
			}
			ir++;
		}
		// We have got some #s left ?
		if (countSharps > 0) {
			// We don't have countSharps > p_springs[is]) . But do we have enough countSharps ? First, try to fill til the needed number
			for ( ; countSharps < p_springs[is] ; countSharps++) {
				if (ir >= p_record.length || p_record[ir] == '.') {
					return {failed : true};
				}
				ir++;
			}
			// Make sure we have just the right number of #s
			if (ir >= p_record.length) {
				is++;
				return {failed : (is != p_springs.length), ir : ir, is : is}
			} else if (p_record[ir] == '#') {
				return {failed : true}
			} else { // A '.' or a '?' right after the #-string. Let's move to the next character
				ir++;
				is++;
				if (is == p_springs.length) {
					// Check we don't have any #s left (Debug note : I forgot this case which caused "??#? 1" to return 2 possibilities...
					for ( ; ir <  p_record.length ; ir++) {
						if (p_record[ir] == '#') {
							return {failed : true};
						}
					}
				}
				countSharps = 0; // Reset countSharps, please ;)
			}
		}
	}
	return {failed : false, ir : ir, is : is}
}

function findAnswer_aux(p_record, p_springs, p_ir, p_is) {
	var answer = -1;
	if (p_is >= p_springs.length) {
		for (var k = p_ir ; k < p_record.length ; k++) {
			if (p_record[k] == "#") {
				return 0;
			}
		}
		return 1;
	} else if (p_ir >= p_record.length) {
		return 0;
	} // Debug note : We needed to make test on p_ir and p_is here because that's how we could test right below ! Anyway, we alternate calls to aux and aux_aux
	
	if (gCombinations[p_is-gOffsetIS][p_ir-gOffsetIR] != -1) {
		return gCombinations[p_is-gOffsetIS][p_ir-gOffsetIR];
	}
	answer = findAnswer_aux_aux(p_record, p_springs, p_ir, p_is);
	gCombinations[p_is-gOffsetIS][p_ir-gOffsetIR] = answer;
	return answer;
}

// Character at position p_ir is assumed to be "?" and either it is the first one or the one before is "." 
function findAnswer_aux_aux(p_record, p_springs, p_ir, p_is) {
	// Tests on p_ir and p_is already made above
	
	// Case p_ir is '.'
	p_record[p_ir] = '.';
	var coors = continueToFirstUnknown(p_record, p_springs, p_ir, p_is);
	p_record[p_ir] = '?';
	const answerEmpty = (coors.failed) ? 0 : findAnswer_aux(p_record, p_springs, coors.ir, coors.is);
	// Case p_ir is '#'
	p_record[p_ir] = '#';
	coors = continueToFirstUnknown(p_record, p_springs, p_ir, p_is);
	p_record[p_ir] = '?';
	const answerFull = (coors.failed) ? 0 : findAnswer_aux(p_record, p_springs, coors.ir, coors.is);	
	return answerEmpty + answerFull;
}

