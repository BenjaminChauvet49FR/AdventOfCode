function testSymetricRowsSwitching1(p_block, p_y) {
	var mustSwitch = true;
	var yU = p_y;
	var yD = p_y+1;
	var x;
	while (yU >= 0 && yD < p_block.length) {
		for (x = 0 ; x < p_block[0].length ; x++) {
			if (p_block[yU][x] != p_block[yD][x]) { // We must switch EXACTLY once !
				if (!mustSwitch) {
					return false;
				} else {
					mustSwitch = false; 
				}
			}
		}			
		yU--;
		yD++;
	}
	// Reached the end. So, did we get the exact needed switch ?
	return !mustSwitch;
}

