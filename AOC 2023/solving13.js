function conclusion_13_1() {
	var block = [];
	var answer = 0;
	for (var i = 0 ; i < rawData.length ; i++) {
		if (rawData[i].length > 0) {
			block.push(rawData[i]);
		} else {
			if (block.length > 0) {
				answer += analyze(block);
				block = [];
			}
		}
	}
	if (block.length > 0) {
		answer += analyze(block);
	}
	return answer;
}

function analyze(p_block) {
	const ry = getSymmetryRow(p_block);
	const rx = getSymmetryColumn(p_block);
	return ry*100+rx; 
}

function getSymmetryColumn(p_block) {
	var iL, iR;
	for (var i = 0 ; i < p_block[0].length-1 ; i++) {
		if (equalColumns(p_block,i, i+1)) {
			iL = i-1;
			iR = i+2;
			while (iL >= 0 && iR < p_block[0].length && equalColumns(p_block, iL, iR)) {
				iL--;
				iR++;
			}
			if (!(iL >= 0 && iR < p_block[0].length)) {
				return i+1; // and not i. That's the rules of the puzzle !
			}
		}
	}
	return 0;
}

function getSymmetryRow(p_block) {
	var iU, iD;
	for (var i = 0 ; i < p_block.length-1 ; i++) {
		if (p_block[i] == p_block[i+1]) {
			iU = i-1;
			iD = i+2;
			while (iU >= 0 && iD < p_block.length && (p_block[iU] == p_block[iD])) {
				iU--;
				iD++;
			}
			if (!(iU >= 0 && iD < p_block.length)) {
				return i+1; // See above
			}
		}
	}
	return 0;
}

function equalColumns(p_block, p_i, p_j) {
	var i = 0;
	while(i < p_block.length && p_block[i].charAt(p_i) == p_block[i].charAt(p_j)) {
		i++;
	}
	return (i == p_block.length);
}

