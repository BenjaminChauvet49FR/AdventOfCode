function someDistance(p_asterI, p_asterJ, p_dist) {
	var x1 = p_asterI.x;
	var y1 = p_asterI.y;
	var x2 = p_asterJ.x;
	var y2 = p_asterJ.y;
	var ix1 = getIndexInListWithAdd(gColumns, x1);
	var iy1 = getIndexInListWithAdd(gRows, y1);
	var ix2 = getIndexInListWithAdd(gColumns, x2);
	var iy2 = getIndexInListWithAdd(gRows, y2);
	var dxB = Math.abs(x2-x1);
	var emptyColumns = Math.max(dxB-Math.abs(ix2-ix1), 0);
	var dyB = Math.abs(y2-y1);
	var emptyRows = Math.max(dyB-Math.abs(iy2-iy1), 0);
	return dxB + dyB + (emptyRows + emptyColumns)*(p_dist-1);
}