// Return true if two claims of these indexes overlap
function noOverlap(p_i, p_j) {
	const xMinI = minMaxData[p_i][0];
	const yMinI = minMaxData[p_i][1];
	const xMaxI = minMaxData[p_i][2];
	const yMaxI = minMaxData[p_i][3];
	const xMinJ = minMaxData[p_j][0];
	const yMinJ = minMaxData[p_j][1];
	const xMaxJ = minMaxData[p_j][2];
	const yMaxJ = minMaxData[p_j][3];
	return (xMaxJ < xMinI) || (xMaxI < xMinJ) || (yMaxJ < yMinI) || (yMaxI < yMinJ)
}