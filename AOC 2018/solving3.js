function place(p_i) {
	return Math.floor(p_i/50);
}

/*
A 20x20 array that contains the indexes of the grids.
[0][x] : 0-49 in y
[1][x] : 50-49 in y...
[11][4] : the 500-549 in y, 150-199 in x
Note : we can swap x and y, actually
*/
function makeArrayIndexes() {
	var result = generateArrangedDoubleEntryArray(20, 20, function(x, y) {return []});
	for (var i = 0 ; i < rawData.length ; i++) {
		for (x = place(minMaxData[i][0]) ; x <= place(minMaxData[i][2]) ; x++) {
			for (y = place(minMaxData[i][1]) ; y <= place(minMaxData[i][3]) ; y++) {
				result[x][y].push(i);
			}
		}
	}
	return result;
}

// Cuts the (p_xMin-p_xMax, p_yMin-p_yMax) region into rectangular slices separated by the indexes
// If the indexes are [3, 8] and the corresponding minMaxes are (20, 30, 40, 41) and (23, 32, 45, 49), slices are (0, 20, 23, 41, 46, 50) in x and (30, 32, 46, 50) ; y=49 being at the border of the region
function makeSlices(p_indexList, p_xMin, p_xMax, p_yMin, p_yMax) {
	slicesX = [p_xMin, p_xMax+1];
	slicesY = [p_yMin, p_yMax+1];
	var minMax;
	for (var i = 0 ; i < p_indexList.length ; i++) {
		minMax = minMaxData[p_indexList[i]];
		if (minMax[0] > p_xMin) {
			shiftIntoListUniqueNumbers(slicesX, minMax[0]);
		}
		if (minMax[2] < p_xMax) {
			shiftIntoListUniqueNumbers(slicesX, minMax[2]+1);
		}
		if (minMax[1] > p_yMin) {
			shiftIntoListUniqueNumbers(slicesY, minMax[1]);
		}
		if (minMax[3] < p_yMax) {
			shiftIntoListUniqueNumbers(slicesY, minMax[3]+1);
		}
	}
	return {xPart : slicesX, yPart : slicesY};
}





