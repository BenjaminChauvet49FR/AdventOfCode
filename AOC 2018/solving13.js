const XLENGTH = rawData[0].length;
const YLENGTH = rawData.length;
var arrayTracks;
var listTracks;
var racers;
const DIR = {
	L : 0,
	U : 1,
	R : 2,
	D : 3
}
const CORNER = {
	LU : 0,
	UR : 1,
	RD : 2,
	DL : 3,
	NONE : 4
}

const TURN = {
	LEFT : 0,
	STRAIGHT : 1,
	RIGHT : 2
}

function getNextTurn(p_turn) {
	return (p_turn+1)%3;
}

function getDirClockwise(p_dir) {
	if (p_dir == 3) {
		return 0;
	}
	return p_dir+1;
}
function getDirCounterClockwise(p_dir) {
	if (p_dir == 0) {
		return 3;
	}
	return p_dir-1;	
}

function getChar(p_x, p_y) {
	return rawData[p_y].charAt(p_x);
}

function init() {
	arrayTracks = generateDoubleEntryArray(XLENGTH, YLENGTH, null);
	listTracks = [];
	racers = [];
	var c;
	for (var y = 0 ; y < YLENGTH ; y++) {
		for (var x = 0 ; x < XLENGTH ; x++) {
			if (getChar(x, y) == '/' && x < XLENGTH-1) {
				c = getChar(x+1, y);
				if (c == '-' || c == '+' || c == '<' || c == '>') {					 // Thankfully, no occurence of "/@" belonging to the same track 
					generateTrackFrom(x, y);
				}
			}
		}
	}
}

const ID_CROSSROAD = -1;

function generateTrackFrom(p_x, p_y) {
	var x = p_x;
	var y = p_y;
	var c = getChar(x, y);
	var spaceCount = 0;
	var xMax, yMax;
	while (c != '@') {
		if (c == '+') {
			addCrossRoadFromH(x, y, listTracks.length, spaceCount, true);
		} else {
			addStraightLineAndMaybeDriver(c, x, y, listTracks.length, 	spaceCount, '>', '<');
		}
		spaceCount++;
		x++;
		c = getChar(x, y);			
	}
	xMax = x;
	while (c != '/') {
		if (c == '+') {
			addCrossRoadFromV(x, y, listTracks.length, spaceCount, true);
		} else {
			addStraightLineAndMaybeDriver(c, x, y, listTracks.length, 	spaceCount, 'v', '^');
		}
		spaceCount++;
		y++;
		c = getChar(x, y);
	}
	yMax = y;
	for (var x = xMax ; x > p_x ; x--) {
		c = getChar(x, y);
		if (c == '+') {
			addCrossRoadFromH(x, y, listTracks.length, spaceCount, false);
		} else {
			addStraightLineAndMaybeDriver(c, x, y, listTracks.length, 	spaceCount, '<', '>');
		}
		spaceCount++;
	}
	for (var y = yMax ; y > p_y ; y--) {
		c = getChar(x, y);
		if (c == '+') {
			addCrossRoadFromV(x, y, listTracks.length, spaceCount, false);
		} else {
			addStraightLineAndMaybeDriver(c, x, y, listTracks.length, 	spaceCount, '^', 'v');
		}
		spaceCount++;
	}
	listTracks.push({
		xMin : p_x,
		xMax : xMax, 
		yMin : p_y,
		yMax : yMax,
		size : 2*(xMax-p_x+yMax-p_y),
		clockwiseGoing : [xMax+yMax, yMax+2*xMax, 0, xMax], // meet space when going clockwise = go left. Then, up, right, down
		counterClockwiseGoing : [xMax, xMax+yMax, yMax+2*xMax, 0]
	});
	arrayTracks[p_y][p_x].corner = CORNER.LU;
	arrayTracks[p_y][xMax].corner = CORNER.UR;
	arrayTracks[yMax][xMax].corner = CORNER.RD;
	arrayTracks[yMax][p_x].corner = CORNER.DL;
}

function addCrossRoadFromH(p_x, p_y, p_id, p_spaceCount, p_rightIsClockwise) {
	if (arrayTracks[p_y][p_x] != null) {
		arrayTracks[p_y][p_x].crossH = p_id,
		arrayTracks[p_y][p_x].spaceCrossH = p_spaceCount,
		arrayTracks[p_y][p_x].rightIsClockwise = p_rightIsClockwise
	} else {
		arrayTracks[p_y][p_x] = {
			id : ID_CROSSROAD,
			idCrossH : p_id,
			idCrossV : -1,
			spaceCrossH : p_spaceCount,
			spaceCrossV : -1,
			downIsClockwise : false,
			rightIsClockwise : p_rightIsClockwise,
			corner : CORNER.NONE,
			racerID : NO_RACER
		}
	}
}

function addCrossRoadFromV(p_x, p_y, p_id, p_spaceCount, p_downIsClockwise) {
	if (arrayTracks[p_y][p_x] != null) {
		arrayTracks[p_y][p_x].crossV = p_id,
		arrayTracks[p_y][p_x].spaceCrossV = p_spaceCount,
		arrayTracks[p_y][p_x].downIsClockwise = p_downIsClockwise
	} else {
		arrayTracks[p_y][p_x] = {
			id : ID_CROSSROAD,
			idCrossH : -1,
			idCrossV : p_id,
			spaceCrossH : -1,
			spaceCrossV : p_spaceCount,
			downIsClockwise : p_downIsClockwise,
			rightIsClockwise : false,
			corner : CORNER.NONE,
			racerID : NO_RACER
		}
	}
}

function charToDirection(p_c) {
	switch(p_c) {
		case '<' : return DIRECTION.LEFT; break;
		case '^' : return DIRECTION.UP; break;
		case '>' : return DIRECTION.RIGHT; break;
		case 'v' : return DIRECTION.DOWN; break;
		default : return null;break;
	}
}

const NO_RACER = -1;
function addStraightLineAndMaybeDriver(p_c, p_x, p_y, p_id, p_spaceCount, p_charDriverCW, p_charDriverCCW) {
	arrayTracks[p_y][p_x] = {
			id : listTracks.length,
			space : p_spaceCount,
			hasRacer : false,
			racerID : NO_RACER, // As there is no imminent crash, no need to initialize them even with spaces with racers on them
			corner : CORNER.NONE // whether it turns or not. For corner spaces, will be overridden when closing the track
	}
	// Add a new racer ?
	if (p_c == p_charDriverCW) {
		racers.push({
			x : p_x,
			y : p_y,
			goesClockwise : true,
			direction : charToDirection(p_c),
			nextTurn : TURN.LEFT
		});
	} else if (p_c == p_charDriverCCW) {
		racers.push({
			x : p_x,
			y : p_y,
			goesClockwise : false,
			direction : charToDirection(p_c),
			nextTurn : TURN.LEFT
		});
	}
}

