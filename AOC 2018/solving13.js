function moveRacer(p_racerId) {
	racer = racers[p_racerId];
	var fX = racer.x;
	var fY = racer.y;
	var found = false;
	if (racer.direction == DIRECTION.LEFT) {
		racer.x--;
		changeRacerDirectionOnTurn(racer, CORNER.LU, DIRECTION.DOWN, CORNER.DL, DIRECTION.UP);
		found = true;
	}
	if (!found && racer.direction == DIRECTION.UP) {
		racer.y--;
		changeRacerDirectionOnTurn(racer, CORNER.UR, DIRECTION.LEFT, CORNER.LU, DIRECTION.RIGHT);
		found = true;
	}
	if (!found && racer.direction == DIRECTION.RIGHT) {
		racer.x++;
		changeRacerDirectionOnTurn(racer, CORNER.RD, DIRECTION.UP, CORNER.UR, DIRECTION.DOWN);
		found = true;
	}
	if (!found && racer.direction == DIRECTION.DOWN) {
		racer.y++;
		changeRacerDirectionOnTurn(racer, CORNER.DL, DIRECTION.RIGHT, CORNER.RD, DIRECTION.LEFT);
	}
	arrayTracks[fY][fX].racerID = NO_RACER;
	if (arrayTracks[racer.y][racer.x].racerID != NO_RACER) {
		const rOther = arrayTracks[racer.y][racer.x].racerID;
		arrayTracks[racer.y][racer.x].racerID = NO_RACER;
		return {x : racer.x, y : racer.y, otherCrasher : rOther}
	}
	arrayTracks[racer.y][racer.x].racerID = p_racerId;
	if (arrayTracks[racer.y][racer.x].id == ID_CROSSROAD) {
		switch(racer.nextTurn) {
			case TURN.LEFT:
				racer.direction = getDirCounterClockwise(racer.direction);
			break;
			case TURN.RIGHT:
				racer.direction = getDirClockwise(racer.direction);
			break;
		}
		racer.nextTurn = getNextTurn(racer.nextTurn);
	}
	return null;
}

function changeRacerDirectionOnTurn(p_racer, p_corner1, p_direction1, p_corner2, p_direction2) {
	if (arrayTracks[p_racer.y][p_racer.x].corner == p_corner1) {
		p_racer.direction = p_direction1;
	} else if (arrayTracks[p_racer.y][p_racer.x].corner == p_corner2) {
		p_racer.direction = p_direction2;		
	}
}

